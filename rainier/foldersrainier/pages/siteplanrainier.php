                    <p>The purpose of this website is to provide information to anyone who wants to visit Mt. Rainier in Washington. The Mt. Rainier tourist can find guide and activities to do in Mt. Rainier. If they have any questions about the park they could easily go to the contact sections and ask anything they want.</p>
                    <h2>Target Audience</h2>  
                    <p>This website is intended for responsible adults, male or female, who are willing to visit Mt. Rainier in Washington. These are people typically from ages 30 – 60 who are looking for an adventure and are looking to plan this adventure. These adults may also have a question in mind to be able to ask the park rangers/owners and are seeking and answer about Mt. Rainier.</p>
